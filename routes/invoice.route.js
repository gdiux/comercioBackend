/** =====================================================================
 *  INVOICE ROUTER 
=========================================================================*/
const { Router } = require('express');
const { check } = require('express-validator');

// MIDDLEWARES
const { validarCampos } = require('../middlewares/validar-campos');
const { validarJWT } = require('../middlewares/validar-jwt');

// CONTROLLERS
const { getInvoice, getInvoiceId, createInvoice, updateInvoice, cancelInvoice } = require('../controllers/invoices.controller');

const router = Router();

/** =====================================================================
 *  POST INVOICE
=========================================================================*/
router.post('/query', validarJWT, getInvoice);
/** =====================================================================
 *  POST INVOICE
=========================================================================*/

/** =====================================================================
 *  GET INVOICE ID
=========================================================================*/
router.get('/:id', validarJWT, getInvoiceId);
/** =====================================================================
 *  GET INVOICE ID
=========================================================================*/

/** =====================================================================
 *  POST CREATE INVOICE
=========================================================================*/
router.post('/', [
        validarJWT
    ],
    createInvoice
);
/** =====================================================================
 *  POST CREATE INVOICE
=========================================================================*/

/** =====================================================================
 *  PUT INVOICE
=========================================================================*/
router.put('/:id', validarJWT, updateInvoice);

/** =====================================================================
 *  CANCEL INVOICE
=========================================================================*/
router.put('/cancel/:id', validarJWT, cancelInvoice);

// EXPORT
module.exports = router;